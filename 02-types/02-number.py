# decinteger
print(type(0))
print(type(1))
print(type(147483647))
print(type(79228162514264337593543950336))
print(type(100_000_000_000))

 # bininteger 0..1
print(type(0b11100101))
print(type(0b_1110_0101))

# octinteger 0..7
print(type(0o123456701234))
print(type(0o1234_5670_1234))

# hexinteger 0..9 a..f
print(type(0xabcdef))  
print(type(0xab_cd_ef_ab_cd_ef))  
