# Booleans

print(str(True))  # 'True'
print(str(False)) # 'False'

# Strings

print(str("false"))  # 'false'
print(str("true"))   # 'true'

print(str(""))       # ''
print(str("other"))  # 'other'

# Int

print(str(0))  # '0'
print(str(1))  # '1'

# Float

print(str(0.0))  # '0.0'
print(str(1.0))  # '1.0'

# Imaginary

print(str(0j))  # '0j'
print(str(1j))  # '1j'

# None

print(str(None))  # 'None'

# Ellipsis

print(str(...))       # 'Ellipsis'
print(str(Ellipsis))  # 'Ellipsis'
