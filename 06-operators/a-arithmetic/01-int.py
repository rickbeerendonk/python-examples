print(f"int: 7 + 2 : {7 + 2} ")  # 9   = addition
print(f"int: 7 - 2 : {7 - 2} ")  # 5   = subtraction
print(f"int: 7 * 2 : {7 * 2} ")  # 14  = multiplication
print(f"int: 7 / 2 : {7 / 2} ")  # 3.5 = true division
print(f"int: 7 // 2: {7 // 2}")  # 3   = floor division
print(f"int: 7 % 2 : {7 % 2} ")  # 1   = modulus
print(f"int: 7 ** 2: {7 ** 2}")  # 49  = exponentiation

# / always returns a float
# // returns an int if both operands are ints, otherwise it returns a float.
